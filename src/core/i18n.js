let locale = normalizeLocale(readStoredLocale());

function readStoredLocale() {
  try {
    return globalThis.localStorage?.getItem(LOCALE_KEY);
  } catch {
    return null;
  }
}

function writeStoredLocale(value) {
  try {
    globalThis.localStorage?.setItem(LOCALE_KEY, value);
  } catch {
    // Storage can be unavailable in tests or strict privacy modes.
  }
}

function normalizeLocale(value) {
  if (value === 'en') return 'en-US';
  return dictionaries[value] ? value : 'zh-CN';
}

export function getLocale() {
  return locale;
}

export function setLocale(nextLocale) {
  locale = normalizeLocale(nextLocale);
  writeStoredLocale(locale);
  if (globalThis.document?.documentElement) {
    document.documentElement.lang = locale;
  }
  globalThis.window?.dispatchEvent(new CustomEvent('xiaocaimi:localechange', { detail: { locale } }));
}

export function toggleLocale() {
  setLocale(locale === 'zh-CN' ? 'en-US' : 'zh-CN');
}

export function t(key) {
  return dictionaries[locale]?.[key] || dictionaries['zh-CN'][key] || key;
}

export function localeOptions() {
  return [
    { value: 'zh-CN', label: dictionaries['zh-CN'].simplifiedChinese },
    { value: 'en-US', label: dictionaries['zh-CN'].english },
  ];
}

if (globalThis.document?.documentElement) {
  document.documentElement.lang = locale;
}
